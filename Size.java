        System.out.println("Range of Byte: "+Short.MIN_VALUE+" to "+Short.MAX_VALUE+"\n=====================================\n");

        System.out.println("Integer: ");
        System.out.println("Size: "+Integer.BYTES+" byte = "+Integer.SIZE+" bits");
        System.out.println("Range of Byte: "+Integer.MIN_VALUE+" to "+Integer.MAX_VALUE+"\n=====================================\n");

        System.out.println("Long: ");
        System.out.println("Size: "+Long.BYTES+" byte = "+Long.SIZE+" bits");
        System.out.println("Range of Byte: "+Long.MIN_VALUE+" to "+Long.MAX_VALUE+"\n=====================================\n");

        System.out.println("Floating point datatypes:");
