        System.out.println("Range of Byte: "+Float.MIN_VALUE+" to "+Float.MAX_VALUE+"\n=====================================\n");

        System.out.println("Double: ");
        System.out.println("Size: "+Double.BYTES+" byte = "+Double.SIZE+" bits");
        System.out.println("Range of Byte: "+Double.MIN_VALUE+" to "+Double.MAX_VALUE+"\n=====================================\n");

