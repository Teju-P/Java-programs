public class PrimitiveDatatypes {
    public static void main(String args[]){
        boolean bool = true; //or false.
        byte b = 12; //byte - 1 byte = 8 bits = -128 to 127
        char c = 'a'; //char - 2 bytes
        short sh = 456; //short = 2 bytes = 16 bits = -(2^16)/2 to (2^16)/2-1 = -32,768 to 32,767
        int i = 4; //4 bytes
        long l = 45612856849L; //8 bytes
        float f = 4.56f; //float = 4 bytes
        double d = 4.56d; //or just 4.56, double = 8 bytes
        System.out.println(bool);
        System.out.println(b);
        System.out.println(c);
        System.out.println(sh);
        System.out.println(i);
        System.out.println(l);
        System.out.println(f);
        System.out.println(d);
        
    }
}
