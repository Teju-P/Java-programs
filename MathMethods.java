public class MathMethods {
    public static void main(String[] args) {
        System.out.println(Math.max(10,-23));
        System.out.println(Math.min(10, -23));
        System.out.println(Math.sqrt(25));
        System.out.println(Math.cbrt(8));
        System.out.println(Math.abs(-4.5));
        System.out.println(Math.round(4.5));
        System.out.println(Math.ceil(4.1));
        System.out.println(Math.ceil(-4.1));
        System.out.println(Math.floor(4.9));
        System.out.println(Math.floor(-4.9));
        System.out.println(Math.sin(3));
        System.out.println(Math.sinh(3));
        System.out.println(Math.hypot(3,4));
        System.out.println(Math.log(20));
        System.out.println(Math.log10(20));
        System.out.println(Math.pow(2,8));
        System.out.println(Math.exp(1));
        System.out.println(Math.signum(-4.7890));
        System.out.println(Math.signum(0));
        System.out.println(Math.signum(4.986));
        System.out.println(Math.copySign(5.79, -1.096));
        System.out.println(Math.toDegrees(Math.PI/4));
        System.out.println(Math.toRadians(30));
    }
}
